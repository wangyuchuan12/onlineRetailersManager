package com.wyc.manager.util;

public class Function {
    private String name;
    private String system_code;
    private String data_code;
    private String pulse;
    private String mozi_wave;
    private String codec;
    public String getCodec() {
        return codec;
    }
    public void setCodec(String codec) {
        this.codec = codec;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getSystem_code() {
        return system_code;
    }
    public void setSystem_code(String system_code) {
        this.system_code = system_code;
    }
    public String getData_code() {
        return data_code;
    }
    public void setData_code(String data_code) {
        this.data_code = data_code;
    }
    public String getPulse() {
        return pulse;
    }
    public void setPulse(String pulse) {
        this.pulse = pulse;
    }
    public String getMozi_wave() {
        return mozi_wave;
    }
    public void setMozi_wave(String mozi_wave) {
        this.mozi_wave = mozi_wave;
    }
}
